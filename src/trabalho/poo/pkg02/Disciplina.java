package trabalho.poo.pkg02;

public class Disciplina {
    private String nomeDisciplina;
    private String gabaritoDisciplina;

    public Disciplina(String nomeDisciplina) {
        this.nomeDisciplina = nomeDisciplina;
    }

    
    
    
    
    public String getNomeDisciplina() {
        return nomeDisciplina;
    }

    public void setNomeDisciplina(String nomeDisciplina) {
        this.nomeDisciplina = nomeDisciplina;
    }

    public String getGabaritoDisciplina() {
        return gabaritoDisciplina;
    }

    public void setGabaritoDisciplina(String gabaritoDisciplina) {
        this.gabaritoDisciplina = gabaritoDisciplina;
    }

}